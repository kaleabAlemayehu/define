export class Definition {
  constructor(wordType, shortDefinition) {
    this.wordType = wordType;
    this.shortDefinition = shortDefinition;
  }
}

export class Synonym {
  constructor(synon) {
    this.synon = synon;
  }
}
export class Antonym {
  constructor(anton) {
    this.anton = anton;
  }
}
